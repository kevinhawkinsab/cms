const slugifyCategory = (value) => {
  return String(value)
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}

const normalizeCategory = (category, index) => {
  if (typeof category === 'object' && category !== null) {
    return {
      _id: category._id || slugifyCategory(category.name || `categoria-${index}`),
      name: category.name || `Categoria ${index + 1}`,
      color: category.color || categoryPalette[index % categoryPalette.length]
    }
  }

  return {
    _id: slugifyCategory(category || `categoria-${index}`),
    name: category,
    color: categoryPalette[index % categoryPalette.length]
  }
}

